package springguru.petclinic.model;

public class HomeAnimal {

    private long id;
    private String name;

    public HomeAnimal() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
